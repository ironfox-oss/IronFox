#!/bin/bash

# This file is expected to be executed in GitLab CI
# DO NOT executed this manually!

set -euo pipefail

# Set-up our environment
bash -x "$(realpath $(dirname "$0"))/env.sh"
source "$(realpath $(dirname "$0"))/env.sh"

# Include version info
source "${IRONFOX_VERSIONS}"

export GENERIC_PACKAGES_URL="${CI_API_V4_URL}/projects/${CI_PROJECT_ID}/packages/generic"

upload_to_package_registry() {
    local file="$1"
    local package_name="$2"
    local file_name="$(basename "${file}")"
    curl --header "PRIVATE-TOKEN: ${GITLAB_CI_API_TOKEN}" \
        --upload-file "${file}" \
        "${GENERIC_PACKAGES_URL}/${package_name}/${IRONFOX_VERSION}/${file_name}"
}

# Extract compressed artifacts
mkdir -p "${IRONFOX_ARTIFACTS}"
for archive in "${IRONFOX_ARTIFACTS}"/*.tar.xz; do
    [ -f "$archive" ] || continue
    echo "Extracting $archive"
    tar xvJf "$archive" -C "$IRONFOX_ARTIFACTS"
done

export BUILD_DIR="${CI_PROJECT_DIR}/build"

mkdir -vp "${BUILD_DIR}"

RELEASE_NOTES_FILE="${BUILD_DIR}/release-notes.md"
CHECKSUMS_FILE="${BUILD_DIR}/asset-checksums.txt"
RELEASE_FILE="${BUILD_DIR}/release.yml"

echo -n "" >"${RELEASE_NOTES_FILE}"
echo -n "" >"${CHECKSUMS_FILE}"

declare -a assets
upload_asset() {
    package_name="$1"
    file="$2"
    file_name="$(basename "${file}")"

    echo "\`${file_name}\`: " >>"${CHECKSUMS_FILE}"
    echo "\`\`\`sh" >>"${CHECKSUMS_FILE}"
    echo "$(sha512sum -b "${file}" | cut -d ' ' -f 1)" >>"${CHECKSUMS_FILE}"
    echo "\`\`\`sh" >>"${CHECKSUMS_FILE}"
    echo '' >>"${CHECKSUMS_FILE}"
    upload_to_package_registry "${file}" "${package_name}"
    assets+=("{\"name\": \"${file_name}\",\"url\": \"${GENERIC_PACKAGES_URL}/${package_name}/${IRONFOX_VERSION}/${file_name}\",\"link_type\": \"package\",\"direct_asset_path\": \"/${file_name}\"}")
}

# Upload packages to package registry
for apk in "${IRONFOX_APK_ARTIFACTS}"/*.apk; do
    package_name="apk"
    upload_asset "${package_name}" "${apk}"

done
for apks in "${IRONFOX_APKS_ARTIFACTS}"/*.apks; do
    package_name="apkset"
    upload_asset "${package_name}" "${apks}"
done

{
    changelog_file="${CI_PROJECT_DIR}/changelogs/${IRONFOX_VERSION}.md"
    if [[ -f "${changelog_file}" ]]; then
        cat "${changelog_file}"
    fi

    echo "# IronFox ${IRONFOX_VERSION}"
    echo '____'
    echo ''

    echo '## Changes'
    echo ''
    echo '- '
    echo ''

    echo '## App Verification'
    echo "To verify the integrity/authenticity of your IronFox installation, in addition to [the checksums](#checksums) provided below, we highly recommend using [\`AppVerifier\`](https://github.com/soupslurpr/AppVerifier):"
    echo ''
    echo "Package ID: \`org.ironfoxoss.ironfox\`"
    echo ''
    echo 'SHA256 hash of signing certificate:'
    echo "\`\`\`sh"
    echo 'C5:E2:91:B5:A5:71:F9:C8:CD:9A:97:99:C2:C9:4E:02:EC:97:03:94:88:93:F2:CA:75:6D:67:B9:42:04:F9:04'
    echo "\`\`\`"
    echo ""

    echo '## Checksums'
    echo ''
    cat ${CHECKSUMS_FILE}
    echo ''

    echo '---'
    echo "_This release was automatically generated by the CI/CD pipeline ([view pipeline](${CI_JOB_URL})) and is guaranteed to be generated from commit [${CI_COMMIT_SHORT_SHA}](${CI_PROJECT_URL}/-/tree/${CI_COMMIT_SHA})._"
    echo ''
} >>"${RELEASE_NOTES_FILE}"

{
    echo "---"
    echo "name: IronFox v${IRONFOX_VERSION}"
    echo "tag-name: v${IRONFOX_VERSION}"
    echo "description: |"
    awk '{print "  " $0}' <"${RELEASE_NOTES_FILE}"
    echo "assets-link:"
    for asset in "${assets[@]}"; do
        echo "  - '${asset}'"
    done
} >"${RELEASE_FILE}"
